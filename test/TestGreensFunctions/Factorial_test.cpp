#include "DefsGf.hpp"
#include <gsl/gsl_sf.h>
#include "Factorial_test.hpp"

// --------------------------------------------------------------------------------------------------------------------------------

int testFactorial()
{
   for (uint n = 0; n < 150; ++n)
   {
      double N = 1;
      for (uint i = 1; i <= n; ++i) N *= i;
      
      double E = gsl_sf_fact(n);
      TINYTEST_ALMOST_EQUAL(E, N, 1E-14*E);
   }
   return 1;
}

